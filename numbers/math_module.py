# rounding off a number
print(round(4.5))

# importing math module
import math

# rounding off to lowest integer
print(math.floor(2.9))

# rounding off to highest integer
print(math.ceil(4.1))

# pi value
print(math.pi)

# euler's number value
print(math.e)

# trigonometric functions
print(math.sin(math.pi/2))
print(math.cos(0))
print(math.sin(math.pi))
print(math.floor(math.sin(math.pi)))
print(math.asin(0))

# pythagoras theorem
print(math.hypot(3,4))

# power
print(math.pow(6,2))

# exponential of euler's number
print(math.exp(2))

# log functions
print(math.log(math.e))
print(math.log10(1000))
print(math.log2(16))

